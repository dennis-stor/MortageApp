public class Prospect
{
    String name;
    double totalLoan;
    double interest;
    int numberOfYears;
    String monthlyPayment;


    public Prospect(String name, double totalLoan, double interest, int numberOfYears, String monthlyPayment)
    {
        this.name = name;
        this.totalLoan = totalLoan;
        this.interest = interest;
        this.numberOfYears = numberOfYears;
        this.monthlyPayment = monthlyPayment;
    }




}